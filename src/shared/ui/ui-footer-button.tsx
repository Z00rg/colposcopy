import clsx from "clsx";
import { useRouter } from "next/router";
import { ButtonHTMLAttributes } from "react";
import { ROUTES } from "../constants/routes";

type UiFooterButtonVariants = "book" | "man" | "check";
export type UiButtonProps = {
  variant: UiFooterButtonVariants;
  active?: boolean;
} & ButtonHTMLAttributes<HTMLButtonElement>;

export function UiFooterButton({
  className,
  variant,
  active,
  ...props
}: UiButtonProps) {
  const variantIcons = {
    book: <Book />,
    man: <Man />,
    check: <Check />,
  };

  const variantActiveIcons = {
    book: <BookActive />,
    man: <ManActive />,
    check: <CheckActive />,
  };

  const variantTexts = {
    book: "Атлас",
    man: "Личный кабинет",
    check: "Тест",
  };

  const variantRoute = {
    book: ROUTES.ATLAS,
    man: ROUTES.HOME,
    check: ROUTES.TEST,
  };

  const router = useRouter();

  return (
    <button
      {...props}
      className={clsx(
        className,
        "cursor-pointer flex flex-col gap-2 items-center justify-center text-[11px] hover:text-[#7BAEE4]",
        active && "text-[#7BAEE4]",
      )}
      onClick={() => (router.push(variantRoute[variant]))}
    >
      {active ?  variantActiveIcons[variant] : variantIcons[variant]}
      {variantTexts[variant]}
    </button>
  );
}

